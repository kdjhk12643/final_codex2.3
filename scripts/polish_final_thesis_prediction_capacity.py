def insert_paragraph_after(paragraph: Paragraph, text: str, style: str | None = None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    if style:
        new_para.style = style
    new_para.add_run(text)
    return new_para


def replace_text(doc: Document, old: str, new: str) -> None:
    for para in doc.paragraphs:
        if para.text == old or para.text.startswith(old[:30]):
            para.text = new
            return
    raise ValueError(f"paragraph not found: {old[:30]}")


def insert_after_text(doc: Document, marker: str, texts: list[str]) -> None:
    for para in doc.paragraphs:
        if para.text == marker or para.text.startswith(marker[:30]):
            current = para
            for text in texts:
                current = insert_paragraph_after(current, text)
            return
    raise ValueError(f"marker not found: {marker[:30]}")


