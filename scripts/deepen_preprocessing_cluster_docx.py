def insert_paragraph_after(paragraph: Paragraph, text: str, style: str | None = None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    if style:
        new_para.style = style
    new_para.add_run(text)
    return new_para


def delete_paragraph(paragraph: Paragraph) -> None:
    p = paragraph._element
    p.getparent().remove(p)
    paragraph._p = paragraph._element = None


def find_paragraph(doc: Document, text: str) -> int:
    for i, para in enumerate(doc.paragraphs):
        if para.text.strip() == text:
            return i
    raise ValueError(f"paragraph not found: {text[:40]}")


def replace_between(doc: Document, start_marker: str, end_marker: str, new_texts: list[str]) -> None:
    start = find_paragraph(doc, start_marker)
    end = find_paragraph(doc, end_marker)
    anchor = doc.paragraphs[start]
    style = doc.paragraphs[start + 1].style if start + 1 < end else None
    for para in list(doc.paragraphs[start + 1 : end]):
        delete_paragraph(para)
    current = anchor
    for text in new_texts:
        current = insert_paragraph_after(current, text, style.name if style else None)


